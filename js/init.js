    request.onreadystatechange = function() {
        if (request.readyState == 4 && request.status == 200) {
            js_onSuccess();
        } else
        if(request.readyState == 4) {
            js_onError(request.response);
        }
    };

    var contactName = document.querySelector("#" + form_id_js + " [name='contactName']").value;
    var contactEmail = document.querySelector("#" + form_id_js + " [name='email']").value;
